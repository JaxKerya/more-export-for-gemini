/**
 * Builds src/vendor/highlight.js — a self-contained payload that the Reader
 * exporter inlines into its standalone HTML *only when the report has code*.
 *
 * It bundles:
 *   - GEP.hljs.js  : the minified highlight.js library (common languages)
 *   - GEP.hljs.css : github (light) + github-dark token colors, scoped to the
 *                    Reader theme system so code colors follow both
 *                    prefers-color-scheme AND the manual [data-theme] toggle.
 *
 * Backgrounds/padding from the upstream themes are dropped: the Reader's own
 * <pre> provides those (var(--code-bg) flips with the theme), so we only keep
 * the per-token text colors/weights and let them ride on our surface.
 *
 * Run:  node scripts/build-hljs-vendor.mjs
 */
import fs from "node:fs/promises";
import path from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const repoRoot = path.resolve(__dirname, "..");
const VERSION = "11.9.0";
const base = `https://cdn.jsdelivr.net/gh/highlightjs/cdn-release@${VERSION}/build`;

async function get(url) {
  const res = await fetch(url);
  if (!res.ok) throw new Error(`fetch ${url} -> ${res.status}`);
  return res.text();
}

/** Split minified CSS into {sel, body} rules (flat themes, no @media). */
function parseRules(css) {
  const rules = [];
  // Strip comments.
  css = css.replace(/\/\*[\s\S]*?\*\//g, "");
  const re = /([^{}]+)\{([^{}]*)\}/g;
  let m;
  while ((m = re.exec(css))) {
    rules.push({ sel: m[1].trim(), body: m[2].trim() });
  }
  return rules;
}

/** Keep only per-token rules (.hljs-…); drop the bare .hljs/code.hljs box rules. */
function tokenRules(css) {
  return parseRules(css).filter((r) => /\.hljs-/.test(r.sel));
}

function prefixSelector(sel, prefix) {
  return sel
    .split(",")
    .map((s) => `${prefix} ${s.trim()}`)
    .join(",");
}

function emit(rules) {
  return rules.map((r) => `${r.sel}{${r.body}}`).join("");
}
function emitPrefixed(rules, prefix) {
  return rules.map((r) => `${prefixSelector(r.sel, prefix)}{${r.body}}`).join("");
}

async function main() {
  const [js, lightCss, darkCss] = await Promise.all([
    get(`${base}/highlight.min.js`),
    get(`${base}/styles/github.min.css`),
    get(`${base}/styles/github-dark.min.css`),
  ]);

  const light = tokenRules(lightCss);
  const dark = tokenRules(darkCss);

  // Neutralize the box: our <pre>/<code> own background + padding so code
  // colors sit on the theme-aware surface (and flip with the manual toggle).
  const neutral = `code.hljs,pre code.hljs{background:transparent;padding:0;color:inherit;}`;
  const css =
    neutral +
    // Light token colors are the default.
    emit(light) +
    // OS dark, unless the reader is manually forced light.
    `@media (prefers-color-scheme:dark){${emitPrefixed(dark, 'html:not([data-theme="light"])')}}` +
    // Manual dark toggle always wins, regardless of OS.
    emitPrefixed(dark, 'html[data-theme="dark"]');

  const out =
    `/* AUTO-GENERATED by scripts/build-hljs-vendor.mjs — do not edit by hand.\n` +
    ` * highlight.js v${VERSION} (common languages) + github/github-dark token\n` +
    ` * colors, scoped to the Reader theme system. Regenerate with:\n` +
    ` *   node scripts/build-hljs-vendor.mjs\n` +
    ` */\n` +
    `(function(){\n` +
    `  var GEP = (window.GEP = window.GEP || {});\n` +
    `  GEP.hljs = {\n` +
    `    version: ${JSON.stringify(VERSION)},\n` +
    `    css: ${JSON.stringify(css)},\n` +
    `    js: ${JSON.stringify(js)}\n` +
    `  };\n` +
    `})();\n`;

  const dest = path.join(repoRoot, "src", "vendor", "highlight.js");
  await fs.writeFile(dest, out, "utf8");
  console.log(`wrote ${path.relative(repoRoot, dest)}`);
  console.log(`  hljs.js : ${(js.length / 1024).toFixed(1)} KiB`);
  console.log(`  hljs.css: ${(css.length / 1024).toFixed(1)} KiB (light ${light.length} + dark ${dark.length} token rules)`);
}

main().catch((e) => { console.error(e); process.exit(1); });
